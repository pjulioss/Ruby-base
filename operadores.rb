# Operadoes aritiméticos
# + = soma
# - = subtrair
# * = multiplicar
# / = divisão
# ** = potencia
# % = modulo ou resto

puts 10 / 7 # 1
puts 10 / 7.0 # 1.4285714285714286, cuidado com o tipo de valor na hora de calcular

# Operadores de comparação
#  = atribuição
# != diferente
# > maior
# < menor
# >= maior ou igual
# <= menor ou igual

puts 10 > 5 #true

# Operadores atributativos, úteis para fazer uma operação matemática rpida
# += 
# -=
# *=
# /=
# %=
# **=
saldo = 1000
salario = 1500
saldo += salario #para somar o valor atual de saldo mais o salario, sem necessidade de se criar outra variavel 

# Operadores lógicos, sempre usar forma de simbolo
# && = and
# || = or
# ! = not